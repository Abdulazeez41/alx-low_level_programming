#include "main.h"

/**
 *	print_diagonal - Print number of lines diagonally
 *	@n: int data input
 *	Return 0 if run sucessful
 */
void print_diagonal(int n)
{
	int i;
	int j;

	if (n <= 0)
	{
		_putchar('\n');
	}
	for (i = 0; i < n; i++)
	{
		for (j = 0; j < i; j++)
		{
			_putchar(' ');
		}
		_putchar('\\');
		_putchar('\n');
	}
}
